    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $model = Spot::findOrFail($request->spot_id);

        $data = ([
            'title' => $request->title,
        ]);

        if ($request->file('image')){
            $fileName = 'gallery-'.time().'.'.$request->image->extension();
            $path = public_path('images/galleries');        

            $data['image'] = $fileName;
            $request->image->move($path, $fileName);
        }

        $model->images()->create($data);
        $model->save();

        return redirect()->back()->with('successMessage', 'Berhasil menambahkan foto');
    }

